sort_by = ['last_active', 'grade', 'prints']


def sort_name(l):
    l.sort(key=lambda x: x[0].lower())


def sort_prints(l):
    l.sort(key=lambda x: x[5])


def sort_wishlisted(l):
    l.sort(key=lambda x: x[4], reverse=True)


def sort_have(l):
    l.sort(key=lambda x: x[2], reverse=True)


def sort_want(l):
    l.sort(key=lambda x: x[3], reverse=True)


def sort_completion(l):
    l.sort(key=lambda x: int(x[7].split('%')[0]), reverse=True)


def sort_active_number(l):
    for result in l:
        if result[6] == 'just now':
            result[6] = '0 just now'
    l.sort(key=lambda x: int(x[6].split(' ')[0]))


def sort_active(l):
    times = ['just now', 'min ago', 'hour ago', 'hours ago', 'day ago', 'days ago', 'month ago', 'months ago',
             'year ago', 'years ago']
    l.sort(key=lambda x: times.index(' '.join(x[6].split(' ')[1:])))


